package com.tour.persistence;

public class StoryImagesVO {
	private int story_images_id;
	private String image;
	private int story_id;

	public int getStory_images_id() {
		return story_images_id;
	}

	public void setStory_images_id(int story_images_id) {
		this.story_images_id = story_images_id;
	}

	public String getImage() {
		return image;
	}

	public void setImage(String image) {
		this.image = image;
	}

	public int getStory_id() {
		return story_id;
	}

	public void setStory_id(int story_id) {
		this.story_id = story_id;
	}

}
