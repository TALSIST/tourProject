package com.tour.persistence;

public class SharedMembersVO {
	private int shared_members_id;
	private int tour_id;
	private int member_id;
	
	public int getShared_members_id() {
		return shared_members_id;
	}
	public void setShared_members_id(int shared_members_id) {
		this.shared_members_id = shared_members_id;
	}
	public int getTour_id() {
		return tour_id;
	}
	public void setTour_id(int tour_id) {
		this.tour_id = tour_id;
	}
	public int getMember_id() {
		return member_id;
	}
	public void setMember_id(int member_id) {
		this.member_id = member_id;
	}
	
}
